<div class="scale">E. Sistem KI</div>
<div class="container" style="margin-left: 13px"><p align="justify">KI merupakan hak privat yang mempunyai ciri khas, dimana seseorang bebas untuk mengajukan permohonan/ pendaftaran karya intelektualnya atau tidak. Disamping itu KI menganut sistem first to file, yaitu suatu sistem pemberian pelindungan yang menganut mekanisme bahwa seseorang yang pertama kali/ lebih dahulu mengajukan permohonan/ pendaftaran dianggap sebagai pemilik hak. Hak eksklusif yang diberikan Negara kepada individu pelaku KI (inventor, pencipta, pendesain dan sebagainya) tiada lain dimaksudkan sebagai penghargaan atas hasil karya/ kreativitas-nya dan agar orang lain terangsang untuk dapat lebih lanjut mengembangkannya lagi, sehingga dengan sistem pelindungan KI tersebut kepentingan masyarakat ditentukan melalui mekanisme pasar.</p></div>

<div class="scale">F. Badan Internasional yang menangani KI</div>
<div class="container" style="margin-left: 13px"><p align="justify">Badan tersebut adalah World Intellectual Property Organization (WIPO), suatu badan khusus PBB, dan Indonesia termasuk salah satu anggota dengan diratifikasinya Paris Convention for the Protection of Industrial Property and Convention Establishing the World Intellectual Property Organization.</p></div>

<div class="scale">G. Kedudukan KI di mata dunia Internasional</div>
<div class="container" style="margin-left: 13px"><p align="justify">Pada saat ini, KI telah menjadi isu yang sangat penting dan mendapat perhatian baik dalam forum nasional maupun internasional. Dimasukkannya TRIPs dalam paket Persetujuan WTO di tahun 1994 menandakan dimulainya era baru perkembangan KI di seluruh dunia. Dengan demikian pada saat ini permasalahan KI tidak dapat dilepaskan dari dunia perdagangan dan investasi. Pentingnya KI dalam pembangunan ekonomi dan perdagangan telah memacu dimulainya era baru pembangunan ekonomi yang berdasar ilmu pengetahuan.</p></div><br>


