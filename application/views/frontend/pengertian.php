<div class="scale">D. Bidang KI</div>
<div class="container" style="margin-left: 13px">Secara garis besar KI menurut kepemilikannya dibagi menjadi 2 (dua) bagian yaitu:</div>
<div class="container" style="margin-left: 13px"><label> 1. &nbsp;&nbsp;Kepemilikan Komunal</label></div>
<div class="container" style="margin-left: 30px"><p align="justify">Dalam perkembangan arus perdagangan bebas serta kemajuan teknologi, komunikasi dan informasi, transaksi perdagangan produk dan jasa semakin cepat dan tak terbatas. Perkembangan di pasar internasional juga ditindaklanjuti dengan kesepakatan perdagangan internasional oleh Negara-negara anggota World Trade Organization (WTO) untuk melancarkan arus distribusi barang, jasa dan investasi dengan mengurangi secara bertahap hambatan-hambatan tarif bea masuk. Hal ini menuntut adanya standard kualitas produk dan jasa, persaingan usaha yang sehat serta adanya komitmen terhadap peraturan perdagangan internasional yang disepakati bersama.</p></div>
<div class="container" style="margin-left: 13px"><label> 2. &nbsp;&nbsp;Kepemilikan Personal</label></div>
<div class="container" style="margin-left: 30px"><p align="justify">Kekayaan Intelektual yang bersifat personal adalah KI yang dimiliki sepenuhnya oleh individu atau kelompok individu dengan atau tanpa mengajukan permohonan kepada Negara untuk mendapatkan hak monopoli atas eksploitasi secara ekonomi. Kepemilikan personal dibagi lagi menjadi 2 (dua) yaitu:</p></div>
<div class="container" style="margin-left: 10px">
