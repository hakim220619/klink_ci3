                            <p align="justify">&nbsp;&nbsp;&nbsp;&nbsp;Secara substantif pengertian KI dapat didefinisikan sebagai kekayaan yang timbul atau lahir dari kemampuan intelektual manusia. Karya-karya yang timbul atau lahir dari kemampuan intelektual manusia dapat berupa karya-karya di bidang teknologi, ilmu pengetahuan, seni dan sastra yang dilahirkan dengan pengorbanan tenaga, waktu dan bahkan biaya. Pengorbanan tersebut menjadikan karya yang dihasilkan memiliki nilai ekonomi. Manfaat lain yang dapat dinikmati adalah melekatnya konsepsi kekayaan (property) terhadap karya-karya intelektual bagi dunia usaha, karya-karya itu dikatakan sebagai aset perusahaan. ari sudut pandang KI, aturan tersebut diperlukan pelindungan hukum dan menimbulkan sikap penghargaan, penghormatan dan memberikan rasa aman serta mewujudkan iklim yang kondusif bagi peningkatan semangat atau gairah untuk menghasilkan karya-karya inovatif, inventif dan produktif.</p><br/>
